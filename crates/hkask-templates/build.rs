//! Build script — auto-discovers per-skill template manifests at compile time.
//!
//! Scans `registry/templates/*/manifest.yaml` and generates a static array
//! of `(skill_name, manifest_yaml)` pairs embedded via `include_str!`.
//! Per-skill manifests are the canonical source of truth (AGENTS.md),
//! eliminating the hand-maintained `bootstrap-registry.yaml` flat index.

use std::env;
use std::fs;
use std::path::PathBuf;

fn main() {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").unwrap());
    let templates_dir = manifest_dir
        .parent()
        .and_then(|p| p.parent())
        .map(|root| root.join("registry/templates"))
        .expect("Could not locate registry/templates from crate manifest dir");

    let mut skills = Vec::new();
    if templates_dir.is_dir() {
        for entry in fs::read_dir(&templates_dir).expect("read registry/templates") {
            let entry = entry.expect("dir entry");
            let manifest_path = entry.path().join("manifest.yaml");
            if manifest_path.is_file() {
                let skill_name = entry
                    .file_name()
                    .to_str()
                    .expect("UTF-8 skill directory name")
                    .to_string();
                skills.push((skill_name, manifest_path));
            }
        }
    }

    skills.sort_by(|a, b| a.0.cmp(&b.0));

    let mut code = String::new();
    code.push_str("// Auto-generated by build.rs — per-skill template manifests.\n");
    code.push_str("// Do not edit manually. Regenerated on every build.\n\n");
    code.push_str("pub(crate) static MANIFEST_YAMLS: &[(&str, &str)] = &[\n");
    for (name, path) in &skills {
        let abs = path.to_str().expect("UTF-8 manifest path");
        code.push_str(&format!("    (\"{name}\", include_str!(\"{abs}\")),\n"));
    }
    code.push_str("];\n");

    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
    fs::write(out_dir.join("manifest_skills.rs"), code).expect("write generated file");

    // Rebuild when manifests change or skill directories are added/removed.
    println!("cargo:rerun-if-changed={}", templates_dir.display());
    for (_, path) in &skills {
        println!("cargo:rerun-if-changed={}", path.display());
    }
}
